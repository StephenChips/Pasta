let escape (s: string): char = 
    match s with
    | "\\\\" -> '\\'
    | "\\\"" -> '"'
    | "\\\'" -> '\''
    | "\\a" -> '\007'
    | "\\b" -> '\008'
    | "\\t" -> '\t'
    | "\\n" -> '\n'
    | "\\v" -> '\011'
    | "\\f" -> '\012'
    | "\\r" -> '\r'
    | _ -> raise (LexingError "unknown Pasta escape.")

